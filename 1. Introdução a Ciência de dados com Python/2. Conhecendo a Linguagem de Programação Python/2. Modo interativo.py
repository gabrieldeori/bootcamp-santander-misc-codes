dir() # retorna lista de nomes no escopo local atual.
dir(100) # Com argumento retorna lista de atributos válidos para o objeto.
help() # Invoca o sistema de ajuda interativa do Python.
help(100) # Invoca o sistema de ajuda interativa do Python para o objeto 100.
help(print) # Invoca o sistema de ajuda interativa do Python para o objeto print.
help(print.__add__) # Invoca o sistema de ajuda interativa do Python para o método add do objeto print.
