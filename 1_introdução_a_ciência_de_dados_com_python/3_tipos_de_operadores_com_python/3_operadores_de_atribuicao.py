# São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável

# | Simples | = |
# | --- | --- |
# | Adição | += |
# | Subtração | -= |
# | Multiplicação | *= |
# | Divisão | /= |
# | Divisão inteira | //= |
# | Módulo | % |
# | Exponenciação | ** |

# Exemplifique

print("Simples")
x = 5
print(x)

print("Adição")
x += 1
print(x)

print("Subtração")
x -= 1
print(x)

print("Multiplicação")
x *= 2
print(x)

print("Divisão")
x /= 2
print(x)

print("Divisão inteira")
x //= 2
print(x)

print("Exponenciação")
x **= 2
print(x)

print("Módulo")
x %= 3
print(x)